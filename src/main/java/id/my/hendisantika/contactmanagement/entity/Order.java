@Entity
@Table(name = "orders")
@Data
@AllArgsConstructor
@NoArgsConstructor
public class Order {

    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long myOrderId;
    private String orderId;
    private double amount;
    private String receipt;
    private String status;

    @ManyToOne
    private User user;

    private String paymentId;
}
